package com.rewards.model;

/**
 * @author gangaraju
 *
 */
public class RewardPoints {

	private String month;
	private long ponts;
	/**
	 * @return the month
	 */
	public String getMonth() {
		return month;
	}
	/**
	 * @param month the month to set
	 */
	public void setMonth(String month) {
		this.month = month;
	}
	/**
	 * @return the ponts
	 */
	public long getPonts() {
		return ponts;
	}
	/**
	 * @param ponts the ponts to set
	 */
	public void setPonts(long ponts) {
		this.ponts = ponts;
	}
	
	
}
